'''
# 2. Create a program that prompts for 2 numbers and then outputs the
# addition, subtraction, multiplication, and division of the first 
# number by the second number.
'''

num1 = int(input("We're going to do some math. What would you like for the first number? "))
num2 = int(input("What about for the second number? "))
print (num1 + num2)
print (num1 - num2)
print (num1 * num2)
print (num1 / num2)